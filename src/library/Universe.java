package library;

/**
 * Enumeration representing different universes or categories in the library system.
 */
public enum Universe {
	/**
	 * Represents no specific universe or category.
	 */
	NONE,

	/**
	 * Represents the music category.
	 */
	MUSIC,

	/**
	 * Represents the youth category.
	 */
	YOUTH,

	/**
	 * Represents the image and map category.
	 */
	IMAGEANDMAP;
}
