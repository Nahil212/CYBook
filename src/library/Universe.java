package library;

/**
 * Represents different universes or categories for library items.
 */
public enum Universe {
	/**
	 * No specific universe.
	 */
	NONE,

	/**
	 * Music-related items.
	 */
	MUSIC,

	/**
	 * Items intended for youth.
	 */
	YOUTH,

	/**
	 * Image and map-related items.
	 */
	IMAGEANDMAP;
}
